let num = 2;

switch(num) {
    case 1 :
        console.log("Number is One");
        break;
    case 2 :
        console.log("Number is Two");
        break;
    case 3 :
        console.log("Number is Three");
        break;
    case 4 :
        console.log("Number is Four");
        break;
    default: 
        console.log("This is Default Case");
}